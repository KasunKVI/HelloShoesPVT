class CustomerDTO {
    constructor(customer_id, name, gender, joined_date, dob, level, points, building_no, lane, city, state, postal_code, email) {
        this.customer_id = customer_id;
        this.name = name;
        this.gender = gender;
        this.joined_date = joined_date;
        this.dob = dob;
        this.level = level;
        this.points = points;
        this.building_no = building_no;
        this.lane = lane;
        this.city = city;
        this.state = state;
        this.postal_code = postal_code;
        this.email = email;
    }
}

